package agencia;

public class passageiro {

	private int acento;
	private String passaport;
	private String type;
	private String Quercarro;
	private String QuerHops;
	
	public int getAcento() {
		return acento;
	}
	public void setAcento(int acento) {
		this.acento = acento;
	}
	public String getPassaport() {
		return passaport;
	}
	public void setPassaport(String passaport) {
		this.passaport = passaport;
	}
	public String getType() {
		return type;
	}
	public void setType(String type) {
		this.type = type;
	}
	public String getQuercarro() {
		return Quercarro;
	}
	public void setQuercarro(String quercarro) {
		Quercarro = quercarro;
	}
	public String getQuerHops() {
		return QuerHops;
	}
	public void setQuerHops(String querHops) {
		QuerHops = querHops;
	}
	
	
	
	
}

